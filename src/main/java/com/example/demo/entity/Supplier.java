package com.example.demo.entity;

public class Supplier {
    private Integer supplierid;

    private Integer commodity;

    private Integer fullscore;

    private String suppliername;

    private String supplierowner;

    private String supplieruser;

    public Integer getSupplierid() {
        return supplierid;
    }

    public void setSupplierid(Integer supplierid) {
        this.supplierid = supplierid;
    }

    public Integer getCommodity() {
        return commodity;
    }

    public void setCommodity(Integer commodity) {
        this.commodity = commodity;
    }

    public Integer getFullscore() {
        return fullscore;
    }

    public void setFullscore(Integer fullscore) {
        this.fullscore = fullscore;
    }

    public String getSuppliername() {
        return suppliername;
    }

    public void setSuppliername(String suppliername) {
        this.suppliername = suppliername;
    }

    public String getSupplierowner() {
        return supplierowner;
    }

    public void setSupplierowner(String supplierowner) {
        this.supplierowner = supplierowner;
    }

    public String getSupplieruser() {
        return supplieruser;
    }

    public void setSupplieruser(String supplieruser) {
        this.supplieruser = supplieruser;
    }
}